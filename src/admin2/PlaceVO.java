package admin2;

public class PlaceVO {
	private String placenumber, placename, placelocation, placedescription, placeprice, placereview;

	public String getPlacenumber() {
		return placenumber;
	}

	public void setPlacenumber(String placenumber) {
		this.placenumber = placenumber;
	}

	public String getPlacename() {
		return placename;
	}

	public void setPlacename(String placename) {
		this.placename = placename;
	}

	public String getPlacelocation() {
		return placelocation;
	}

	public void setPlacelocation(String placelocation) {
		this.placelocation = placelocation;
	}

	public String getPlacedescription() {
		return placedescription;
	}

	public void setPlacedescription(String placedescription) {
		this.placedescription = placedescription;
	}

	public String getPlaceprice() {
		return placeprice;
	}

	public void setPlaceprice(String placeprice) {
		this.placeprice = placeprice;
	}

	public String getPlacereview() {
		return placereview;
	}

	public void setPlacereview(String placereview) {
		this.placereview = placereview;
	}
}
